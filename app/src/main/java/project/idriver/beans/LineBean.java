public class LineBean {
    /**
     * the select start and end point bean
     */
    public String start;
    public String end;

    public void setStart(String start) {
        this.start = start;
    }
    public String getStart() {
        return this.start;
    }

    public void setEnd(String end) {
        this.end = end;
    }
    public String getEnd() {
        return this.end;
    }
}
